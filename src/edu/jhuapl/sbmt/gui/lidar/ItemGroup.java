package edu.jhuapl.sbmt.gui.lidar;

/**
 * Enum which defines the group of items to apply an operation to.
 */
enum ItemGroup
{
	/**
	 * Type which corresponds to no items.
	 */
	None,

	/**
	 * Type which corresponds to all available items.
	 */
	All,

	/**
	 * Type which corresponds to the selected items.
	 */
	Selected,

	/**
	 * Type which corresponds to the visible items.
	 */
	Visible,
}